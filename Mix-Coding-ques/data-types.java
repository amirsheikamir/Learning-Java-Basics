 // int -> integer type values 
        // double -> decimal numbers 
        // String --> English sentence or English word       
        // long --> big integer values 
        // char --> Single letter or character 
        // boolean -> true or false
        
        // Inside System.out.println(variableName); -> valueOfVariable will be printed 
        
        // float can store around  7 - 8 digits after the decimal 
        // double can store around 15 digit after the decimal 
        
        // all classes they have first Letter Capital 
class Main {
    public static void main(String[] args) {
        
        int a = 500;
        Long b = 5665655531531515L;
        char c = 'S';
        String d = "Hacker Fail to do Job";
        double e = 5.456D;
        float z = 38.256F;
        // boolean f = ;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(z);
    }
}